package variable;

public class Var3 {
  public static void main(String[] args) {
    // 변수 선언, 변수만 생성
    int a ;
    int b , c , d ; // 여러개 선언이 가능

    a = 10;
    b = 20;
    c = 30;
    d = 40;

    // 비어있는 변수는 출력불가
    System.out.println("a : "+a);
    System.out.println("b : "+b);
    System.out.println("c : "+c);
    System.out.println("d : "+d);
  }
}
