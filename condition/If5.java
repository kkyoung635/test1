public class If5 {
  public static void main(String[] args) {
    int num = -1;
    if (num > 0) {
      System.out.println("양수");
    } if (num < 0) {
      System.out.println("음수");
    } if (num == 0) {
      System.out.println("0");
    }
  }
}

